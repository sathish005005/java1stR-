package com.sathish.loop;

public class do_while_loop {

	public static void main(String[] args) {
		//here if condition is false it can run one time
		//if condition is true it can run the given times

		int number =4;
		do {
			System.out.println("do while loop");
			number--;
		}while (number>0);
		
		//another ex
		int number1 = 5;
		do {
			System.out.println("do while loop1");
			number--;
		}while(number>10);

	}

}
