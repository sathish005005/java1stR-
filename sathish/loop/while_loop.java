package com.sathish.loop;

public class while_loop {

	public static void main(String[] args) {

		//if condition is false  not single time will show in while loop concept
		  //similar to for but here the initial value is given out side
		int number = 3;
		while (number>0) {
			System.out.println("while loop");
			number--;
		}
		
	}

}
