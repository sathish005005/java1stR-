package com.sathish.loop;

public class for_loop {

	public static void main(String[] args) {

		
		//forloop
		 for (int initial =0 ; initial <5 ; initial++) {
			 System.out.println("hi,gi");
		 }
		 
		 //other example
		 for (int initial =6 ; initial >0 ; initial --) {
			 System.out.println("hi,bro");
		 }

	}

}
